package ed.escuelaing.arep.calculator;

/**
 * Class that does the operation of Trigonometric functions
 * @author Maria Fernanda Hernandez Vargas
 */
public class Calculate {
    /**
     * Methos that calculates sin function
     * @param value
     * @return
     */
    public double sin (double value){
        return Math.sin(value);
    }

    /**
     * Methos that calculates cos function
     * @param value
     * @return
     */
    public double cos (double value){
        return Math.cos(value);
    }

    /**
     * Methos that calculates tan function
     * @param value
     * @return
     */
    public double tan (double value){
        return Math.tan(value);
    }
}
